
public class Do_While {
	public static void main(String[] args)
	{
		
		int a = 1, n = 5;

		do{
			System.out.println(a);
			a++;
			
		} while(a <= n);	
		
		
		
//2nd program
		
		int i = 1, j = 1;
		do {
			i++;
			j++;
			System.out.println("i=" + i + " and j=" + j);
		    } while (i < 10 || j < 10);

	}
}
